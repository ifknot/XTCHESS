#ifndef ASCII_CONTROL_CODES_H
#define ASCII_CONTROL_CODES_H

#define ASCII_NUL  0x00 // Null
#define ASCII_BEL  0x07 // Bell
#define ASCII_BS   0x08 // Backspace
#define ASCII_HT   0x09 // Horizontal Tab
#define ASCII_LF   0x0A // Line Feed
#define ASCII_VT   0x0B // Vertical Tab
#define ASCII_FF   0x0C // Form Feed (clear screen)
#define ASCII_CR   0x0D // Carriage Return
#define ASCII_SO   0x0E // Shift Out (alternate charset)
#define ASCII_SI   0x0F // Shift In (normal charset)
#define ASCII_ESC  0x1B // Escape (start sequences)
#define ASCII_DEL  0x7F // Delete

#endif
